print("-" * 10, "Cetak angka 10 - 20", "-" * 10)
angka = 10
while(angka <= 20):
    print("Angka", angka)
    angka += 1

print("-" * 10, "Cetak bilangan genap dari angka 10 - 20", "-" * 10)
angka = 10
while(angka <= 20):
    if(angka % 2 == 0):
        print("Angka", angka)
    angka += 1

print("-" * 10, "Cetak angka 10 - 1", "-" * 10)
angka = 10
while(angka >= 1):
    print("Angka", angka)
    angka -= 1

print("-" * 10, "Cetak bilangan ganjil dari angka 10 - 20", "-" * 10)
angka = 10
while(angka <= 20):
    if(angka % 2 == 1):
        print("Angka", angka)
    angka += 1
